/** Funcion que devuelve la suma entre dos numeros
 *
 * Pide el primer sumando
 * Pide el segundo sumando
 * Devuelve el resultado
 *
 */
float funcionSuma(float operandoUno, float operandoDos); //prototipo de la funcion suma.

 /** Funcion que devuelve la resta entre dos numeros
 *
 * Pide el minuendo
 * Pide el sustraendo
 * Devuelve el resultado de la resta entre los dos numeros ingresados
 *
 */
float funcionResta(float operandoUno, float operandoDos); //prototipo de la funcion resta.

 /** Funcion que devuelve la division entre dos numeros
 *
 * Pide el dividendo
 * Pide el divisor (no podremos ingresarle un cero)
 * Devuelve el cociente entre los dos numeros ingresados
 *
 */
float funcionDivision(float operandoUno, float operandoDos); //prototipo de la funcion division.


 /** Funcion que devuelve el producto entre dos numeros
 *
 * Pide el primer factor
 * Pide el segundo factor
 * Devuelve el producto entre los dos numeros ingresados
 *
 */
float funcionMultiplicacion(float operandoUno, float operandoDos); //prototipo de la funcion multiplicacion.

 /** Pide un número al usuario y devuelve el factorial de operandoUno y operandoDos
 *
 * Pide el primer numero
 * Pide el segundo numero
 * Devuelve el factorial de operandoUno y de operandoDos (si ingresamos numeros negativos no devuelve nada)
 *
 */
long int factorial (long int numero); //prototipo de la funcion factorial.


